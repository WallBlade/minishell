#include "minishell.h"

void    env_print(char **env, int fd)
{
    int i;
    int j;

    i = 0;
    j = 0;
    while (env && env[i])
    {
		j = 0;
		while (env[i][j])
			write(fd, &env[i][j++], 1);
		write(fd, "\n", 1);
		i++;
    }
}
