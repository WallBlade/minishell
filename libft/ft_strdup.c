#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*dup;

	dup = collect(sizeof(char) * (ft_strlen(src) + 1));
	if (!dup)
		return (0);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
